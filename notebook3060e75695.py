# Patch Embedding
# ----------------------------
class PatchEmbed(nn.Module):
    def __init__(self):
        super().__init__()
        self.proj = nn.Conv2d(3, 96, kernel_size=4, stride=4)

    def forward(self, x):
        x = self.proj(x)
        B, C, H, W = x.shape
        return x.flatten(2).transpose(1,2), H, W


# ----------------------------
# Swin Transformer (FULL)
# ----------------------------
class SwinTransformer(nn.Module):
    def __init__(self, num_classes=4):
        super().__init__()

        self.patch_embed = PatchEmbed()

        # Stage 1 (96 dim, depth 2)
        self.stage1 = nn.ModuleList([
            SwinBlock(96, 3, 7, 0),
            SwinBlock(96, 3, 7, 3)
        ])

        self.merge1 = PatchMerging(96)

        # Stage 2 (192 dim, depth 2)
        self.stage2 = nn.ModuleList([
            SwinBlock(192, 6, 7, 0),
            SwinBlock(192, 6, 7, 3)
        ])

        self.merge2 = PatchMerging(192)

        # Stage 3 (384 dim, depth 6)
        self.stage3 = nn.ModuleList([
            SwinBlock(384, 12, 7, 0 if i % 2 == 0 else 3)
            for i in range(6)
        ])

        self.merge3 = PatchMerging(384)

        # Stage 4 (768 dim, depth 2)
        self.stage4 = nn.ModuleList([
            SwinBlock(768, 24, 7, 0),
            SwinBlock(768, 24, 7, 3)
        ])

        self.norm = nn.LayerNorm(768)
        self.head = nn.Linear(768, num_classes)

    def forward(self, x):
        x, H, W = self.patch_embed(x)

        for blk in self.stage1:
            x = blk(x, H, W)

        x = self.merge1(x, H, W)
        H, W = H//2, W//2

        for blk in self.stage2:
            x = blk(x, H, W)

        x = self.merge2(x, H, W)
        H, W = H//2, W//2

        for blk in self.stage3:
            x = blk(x, H, W)

        x = self.merge3(x, H, W)
        H, W = H//2, W//2

        for blk in self.stage4:
            x = blk(x, H, W)

        x = self.norm(x)
        x = x.mean(dim=1)

        return self.head(x)

